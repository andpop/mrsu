<?php

$a = 1;

$f = function () use ($a)
{
    echo "a = ${a}";
};

$f();   // 1

echo "\n========================================\n";

$a = 2;

$f();   // 1


echo "\n========================================\n";

$f2 = function () use (&$a)
{
    echo "a = ${a}";
};

$f2();   // 2

echo "\n========================================\n";

$a = 3;  // 3

$f2();

